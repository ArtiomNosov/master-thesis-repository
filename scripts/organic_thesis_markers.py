#!/usr/bin/env python3
"""Rewrite blunt SAT prefix labels into organic Russian prose (thesis draft)."""
from __future__ import annotations

import re
from pathlib import Path

THESIS = (
    Path(__file__).resolve().parents[1]
    / "docs/obsidian/thesis/[USED] thesis_draft_final_ru.md"
)

REPLACEMENTS = [
    (r"^(\d+)\. Реализовано в работе: ", r"\1. В прототипе реализован "),
    (r"^(\d+)\. Концептуальный компонент: ", r"\1. На концептуальном уровне описан "),
    (r"^(\d+)\. Архитектурное допущение: ", r"\1. Для масштабирования допускается, что "),
    (r"^Архитектурный подход: ", "С точки зрения целевой архитектуры, "),
    (r"^Архитектурное допущение: ", "Как проектное допущение дальнейшего развития, "),
    (r"^Концептуальный компонент: ", "Концептуально в системе предусмотрен "),
    (r"^Реализовано в работе: ", "В созданном прототипе "),
    (r"^Исследовано в работе: ", "В эмпирической части работы "),
    (r"^Экспериментально исследовано: ", "Экспериментальная проверка подтвердила, что "),
    (r"\. Реализовано в работе: ", ". В прототипе "),
    (r"\. Исследовано в работе: ", ". В ходе подготовки данных "),
    (r"\. Экспериментально исследовано: ", ". На отложенной выборке показано, что "),
    (r"\. Архитектурное допущение: ", ". Для масштабирования рассматривается допущение, что "),
    (r"\. Архитектурный подход: ", ". Архитектурно "),
    (r"\. Концептуальный компонент: ", ". Концептуально "),
]


def main() -> None:
    text = THESIS.read_text(encoding="utf-8")
    for pattern, repl in REPLACEMENTS:
        text = re.sub(pattern, repl, text, flags=re.MULTILINE)
    THESIS.write_text(text, encoding="utf-8")
    print(f"Updated {THESIS}")


if __name__ == "__main__":
    main()
