def load_metrics() -> dict:
    metrics_path = REPO / "experiments" / "results" / "full_test_ranking_metrics.json"
    local_demo_path = REPO / "experiments" / "results" / "local_demo_smoke_report.json"
    eval_csv_path = REPO / "experiments" / "models" / "bi_encoder_rubert_tiny2" / "eval" / "binary_classification_evaluation_val-binary_results.csv"

    metrics = json.loads(metrics_path.read_text(encoding="utf-8"))
    smoke = json.loads(local_demo_path.read_text(encoding="utf-8-sig"))

    eval_rows: list[dict] = []
    if eval_csv_path.exists():
        with eval_csv_path.open("r", encoding="utf-8") as f:
            reader = csv.DictReader(f)
            for row in reader:
                eval_rows.append(row)

    return {"ranking": metrics, "smoke": smoke, "eval_rows": eval_rows}


